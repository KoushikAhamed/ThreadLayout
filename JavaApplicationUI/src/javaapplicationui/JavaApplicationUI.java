    public void showAnim() {
        if (!timer.isRunning()) {
            xofAnimatePanel = 30;
            timer.start();
        } else {
            timer.stop();
        }
    }

    public void hideAnim() {
        if (!timer.isRunning()) {
            xofAnimatePanel = -30;
            timer.start();
        } else {
            timer.stop();
        }
    }
